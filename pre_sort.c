#include "push_swap.h"

int	median(t_stack *stk_a)
{
	int *tab;
	int i;

	i = 0;
	while (stk_a->next)
	{
		tab[i] = stk_a->content;
		stk_a = stk_a->next;
		i++;
	}
	
}